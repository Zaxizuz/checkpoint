reset: {
    .label sc = SCREEN+$28
    .label __5 = 8
    .label __6 = 8
    .label __7 = 8
    .label __9 = $a
    .label __11 = $c
    .label k = 2
    .label i = 4
    .label l = 6
    lda #$14
    sta VIC_MEMORY
    ldx #' '
    lda #<SCREEN
    sta.z memset.str
    lda #>SCREEN
    sta.z memset.str+1
    lda #<$28*$19
    sta.z memset.num
    lda #>$28*$19
    sta.z memset.num+1
    jsr memset
    lda #<0
    sta.z k
    sta.z k+1
  // scrolling text
  __b1:
    lda.z k+1
    cmp #>$3e8
    bcc __b2
    bne !+
    lda.z k
    cmp #<$3e8
    bcc __b2
  !:
    jsr exit_hypervisor
    rts
  __b2:
    lda.z k
    sta.z i
    lda.z k+1
    sta.z i+1
  __b4:
    lda.z i+1
    cmp #>$3e8
    bcc __b5
    bne !+
    lda.z i
    cmp #<$3e8
    bcc __b5
  !:
    ldx #' '
    lda #<SCREEN
    sta.z memset.str
    lda #>SCREEN
    sta.z memset.str+1
    lda #<$28*$19
    sta.z memset.num
    lda #>$28*$19
    sta.z memset.num+1
    jsr memset
    inc.z k
    bne !+
    inc.z k+1
  !:
    jmp __b1
  __b5:
    lda.z i+1
    lsr
    sta.z __5+1
    lda.z i
    ror
    sta.z __5
    lsr.z __5+1
    ror.z __5
    lsr.z __5+1
    ror.z __5
    lsr.z __5+1
    ror.z __5
    asl.z __6
    rol.z __6+1
    asl.z __6
    rol.z __6+1
    asl.z __6
    rol.z __6+1
    asl.z __6
    rol.z __6+1
    lda.z i
    sec
    sbc.z __7
    sta.z __7
    lda.z i+1
    sbc.z __7+1
    sta.z __7+1
    lda.z __7
    tax
    lda #<COLS
    clc
    adc.z i
    sta.z __9
    lda #>COLS
    adc.z i+1
    sta.z __9+1
    // setting colour for each address
    txa
    ldy #0
    sta (__9),y
    lda #<sc
    clc
    adc.z i
    sta.z __11
    lda #>sc
    adc.z i+1
    sta.z __11+1
    // setting content
    lda #'!'
    sta (__11),y
    ldx #0
  //slowing down computer
  __b7:
    cpx #$1e
    bcc b1
    lda #$1e
    clc
    adc.z i
    sta.z i
    bcc !+
    inc.z i+1
  !:
    jmp __b4
  b1:
    lda #<1
    sta.z l
    lda #>1
    sta.z l+1
  __b9:
    lda.z l+1
    cmp #>$3e8
    bcc __b11
    bne !+
    lda.z l
    cmp #<$3e8
    bcc __b11
  !:
    inx
    jmp __b7
  __b11:
    inc.z l
    bne !+
    inc.z l+1
  !:
    jmp __b9
}
// Copies the character c (an unsigned char) to the first num characters of the object pointed to by the argument str.
// memset(void* zeropage(6) str, byte register(X) c, word zeropage(4) num)
memset: {
    .label end = 4
    .label dst = 6
    .label num = 4
    .label str = 6
    lda.z num
    bne !+
    lda.z num+1
    beq __breturn
  !:
    lda.z end
    clc
    adc.z str
    sta.z end
    lda.z end+1
    adc.z str+1
    sta.z end+1
  __b2:
    lda.z dst+1
    cmp.z end+1
    bne __b3
    lda.z dst
    cmp.z end
    bne __b3
  __breturn:
    rts
  __b3:
    txa
    ldy #0
    sta (dst),y
    inc.z dst
    bne !+
    inc.z dst+1
  !:
    jmp __b2
}
